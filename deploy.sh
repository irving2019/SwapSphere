# Docker —Ä–∞–∑–≤–µ—Ä—Ç—ã–≤–∞–Ω–∏–µ
deploy_with_docker() {
    print_status "–†–∞–∑–≤–µ—Ä—Ç—ã–≤–∞–Ω–∏–µ —Å Docker..."
    
    if [ "$DOCKER_AVAILABLE" = true ]; then
        echo "–í—ã–±–µ—Ä–∏—Ç–µ —Ä–µ–∂–∏–º:"
        echo "1) –†–∞–∑—Ä–∞–±–æ—Ç–∫–∞ (docker-compose.dev.yml)"
        echo "2) –ü—Ä–æ–¥–∞–∫—à–Ω (docker-compose.yml)"
        read -r DOCKER_MODE
        
        if [ "$DOCKER_MODE" = "1" ]; then
            docker-compose -f docker-compose.dev.yml up --build
        else
            docker-compose up --build
        fi
    else
        print_error "Docker –Ω–µ –¥–æ—Å—Ç—É–ø–µ–Ω"
        exit 1
    fi
}

# –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é
main_menu() {
    echo ""
    echo "–í—ã–±–µ—Ä–∏—Ç–µ —Ä–µ–∂–∏–º —Ä–∞–∑–≤–µ—Ä—Ç—ã–≤–∞–Ω–∏—è:"
    echo "1) –õ–æ–∫–∞–ª—å–Ω–∞—è —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–∞"
    echo "2) Docker (—Ä–∞–∑—Ä–∞–±–æ—Ç–∫–∞)"
    echo "3) Docker (–ø—Ä–æ–¥–∞–∫—à–Ω)"
    echo "4) –¢–æ–ª—å–∫–æ —Ç–µ—Å—Ç—ã"
    read -r DEPLOYMENT_MODE
    
    case $DEPLOYMENT_MODE in
        1)
            check_requirements
            setup_venv
            install_dependencies
            setup_database
            collect_static
            run_tests
            start_development_server
            ;;
        2)
            check_requirements
            DOCKER_AVAILABLE=true
            deploy_with_docker
            ;;
        3)
            check_requirements
            DOCKER_AVAILABLE=true
            deploy_with_docker
            ;;
        4)
            check_requirements
            setup_venv
            install_dependencies
            run_tests
            ;;
        *)
            print_error "–ù–µ–≤–µ—Ä–Ω—ã–π –≤—ã–±–æ—Ä"
            exit 1
            ;;
    esac
}

# –ó–∞–ø—É—Å–∫ –≥–ª–∞–≤–Ω–æ–≥–æ –º–µ–Ω—é
